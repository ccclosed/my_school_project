// ── Console ─────────────────────────────────────────────────────────────

pub struct FbConsole {
    fb: *mut u8,
    width: u32, height: u32, pitch: u32, bpp: u8,
    cols: u32, rows: u32,
    cur_x: u32, cur_y: u32,
    fg: u32, bg: u32,
}

// Raw pointer — no Mutex needed since init happens before interrupts
static mut FB: *mut FbConsole = core::ptr::null_mut();

impl FbConsole {
    unsafe fn draw_pixel(&self, x: u32, y: u32, color: u32) {
        if x >= self.width || y >= self.height { return; }
        let off = y as usize * self.pitch as usize + x as usize * (self.bpp as usize / 8);
        if self.bpp == 32 {
            ptr::write_volatile(self.fb.add(off) as *mut u32, color);
        } else if self.bpp == 24 {
            ptr::write_volatile(self.fb.add(off), (color & 0xFF) as u8);
            ptr::write_volatile(self.fb.add(off+1), ((color>>8)&0xFF) as u8);
            ptr::write_volatile(self.fb.add(off+2), ((color>>16)&0xFF) as u8);
        }
    }

    fn draw_glyph(&self, gx: u32, gy: u32, fg: u32, bg: u32, g: &Glyph) {
        for row in 0..16 {
            let bits = g[row];
            for col in 0..8 {
                let c = if (bits >> (7 - col)) & 1 != 0 { fg } else { bg };
                unsafe { self.draw_pixel(gx + col as u32, gy + row as u32, c); }
            }
        }
    }

    fn scroll(&mut self) {
        let row_bytes = self.pitch as usize * 16;
        unsafe { ptr::copy_nonoverlapping(self.fb.add(row_bytes), self.fb, (self.rows as usize - 1) * row_bytes); }
        self.cur_y = self.rows - 1;
        self.cur_x = 0;
        for x in 0..self.cols { self.draw_glyph(x * 8, self.cur_y * 16, self.fg, self.bg, glyph(b' ')); }
    }

    fn clear(&mut self) {
        for y in 0..self.rows {
            for x in 0..self.cols { self.draw_glyph(x*8, y*16, self.fg, self.bg, glyph(b' ')); }
        }
        self.cur_x = 0; self.cur_y = 0;
    }

    fn newline(&mut self) { self.cur_x = 0; self.cur_y += 1; if self.cur_y >= self.rows { self.scroll(); } }

    fn write_byte(&mut self, byte: u8) {
        match byte {
            b'\n' => self.newline(),
            b'\r' => self.cur_x = 0,
            b'\t' => { self.cur_x = (self.cur_x + 8) & !7; if self.cur_x >= self.cols { self.newline(); } }
            _ => {
                if self.cur_x >= self.cols { self.newline(); }
                self.draw_glyph(self.cur_x * 8, self.cur_y * 16, self.fg, self.bg, glyph(byte));
                self.cur_x += 1;
            }
        }
    }

    fn write_str(&mut self, s: &str) { for &b in s.as_bytes() { self.write_byte(b); } }
}

impl fmt::Write for FbConsole {
    fn write_str(&mut self, s: &str) -> fmt::Result { self.write_str(s); Ok(()) }
}

// ── Multiboot2 parsing ──────────────────────────────────────────────────

pub fn init() -> bool {
    let magic = unsafe { ptr::addr_of!(mb2_magic).read_volatile() };
    let info = unsafe { ptr::addr_of!(mb2_info).read_volatile() };

    if magic != 0x36D76289 || info == 0 { return false; }

    let total = unsafe { &*(info as *const u32) };
    let mut off = 8usize;

    while off + 8 <= *total as usize {
        let tag_type = unsafe { *((info as *const u8).add(off) as *const u32) };
        let tag_size = unsafe { *((info as *const u8).add(off + 4) as *const u32) };
        if tag_type == 0 || tag_size == 0 { break; }

        if tag_type == 8 {
            let addr = unsafe { *((info as *const u8).add(off + 8) as *const u64) };
            let pitch = unsafe { *((info as *const u8).add(off + 16) as *const u32) };
            let w = unsafe { *((info as *const u8).add(off + 20) as *const u32) };
            let h = unsafe { *((info as *const u8).add(off + 24) as *const u32) };
            let bpp = unsafe { *((info as *const u8).add(off + 28) as *const u8) };

            if addr != 0 && w > 0 && h > 0 {
                let cols = w / 8;
                let rows = h / 16;
                info!("fb: {}x{}x{}bpp, {}x{} chars", w, h, bpp, cols, rows);
                unsafe {
                    FB = alloc::boxed::Box::into_raw(alloc::boxed::Box::new(FbConsole {
                        fb: addr as *mut u8, width: w, height: h, pitch, bpp,
                        cols, rows, cur_x: 0, cur_y: 0, fg: 0x00AAAAAA, bg: 0x00000000,
                    }));
                    (&mut *FB).clear();
                }
                return true;
            }
        }
        off += tag_size as usize;
        off = (off + 7) & !7;
    }
    debug!("fb: no framebuffer tag");
    false
}

pub fn write_fmt(args: core::fmt::Arguments) {
    use core::fmt::Write;
    unsafe {
        if !FB.is_null() { let _ = (&mut *FB).write_fmt(args); }
    }
}

pub fn cmd_fb() {
    unsafe {
        if !FB.is_null() {
            let c = &*FB;
            println!("fb: {}x{}x{}bpp {}x{} chars", c.width, c.height, c.bpp, c.cols, c.rows);
        } else {
            println!("fb: not initialized (use QEMU -vga std)");
        }
    }
}
